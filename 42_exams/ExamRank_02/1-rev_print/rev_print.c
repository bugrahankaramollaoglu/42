#include <unistd.h>

int main(int ac, char **av)
{
    int len = 0;
    while (av[1][len])
        len++;
    if (ac == 2)
        while (len--)
            write(1, &av[1][len], 1);
    write(1, "\n", 1);
    return 0;
}
